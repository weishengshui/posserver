public class DeliveryWithWrongStatusException extends Exception {

	private static final long serialVersionUID = 4239086470852945217L;

	public DeliveryWithWrongStatusException() {
		super();
	}

	public DeliveryWithWrongStatusException(String message, Throwable cause) {
		super(message, cause);
	}

	public DeliveryWithWrongStatusException(String message) {
		super(message);
	}

	public DeliveryWithWrongStatusException(Throwable cause) {
		super(cause);
	}

}
